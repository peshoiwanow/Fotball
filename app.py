        st.markdown("<div class='section-header'>🚑 2. Кадрова Ситуация и Ротации</div>", unsafe_allow_html=True)
        st.markdown(f"<div class='analysis-body'>{m['injuries']}</div>", unsafe_allow_html=True)
        
